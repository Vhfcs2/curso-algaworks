
public class operadoresdeatribuiçao {

	public static void main(String[] args) {
		Integer numero =  7;
		
		numero = numero + 4;
		System.out.println("número + 4: " + numero );
		
		numero += 4; 
		System.out.println("número += 4: " + numero );
		
		numero -= 4; 
		System.out.println("número -= 4: " + numero );
		

	
	}
	
}
